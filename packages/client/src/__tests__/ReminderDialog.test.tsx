/**
 * リマインダー機能のフロントエンドテスト
 *
 * テスト対象:
 *   - ReminderDialog: リマインダー設定ダイアログ
 *   - ReminderList: リマインダー一覧
 *   - リマインダー通知: Socket.IO経由の通知受信
 */

import { describe, it } from 'vitest';

describe('ReminderDialog - リマインダー設定ダイアログ', () => {
  describe('表示', () => {
    it('ダイアログが開くと時間選択肢（30分後・1時間後・明日）が表示される');
    it('メッセージのプレビューが表示される');
  });

  describe('リマインダー設定', () => {
    it('30分後を選択して設定するとAPIが呼ばれる');
    it('1時間後を選択して設定するとAPIが呼ばれる');
    it('明日を選択して設定するとAPIが呼ばれる');
    it('設定成功後にダイアログが閉じる');
    it('設定失敗時にエラーメッセージが表示される');
  });
});

describe('ReminderList - リマインダー一覧', () => {
  describe('表示', () => {
    it('設定済みリマインダーが一覧表示される');
    it('リマインダーがない場合は空状態が表示される');
    it('各リマインダーにメッセージ・通知予定時刻が表示される');
  });

  describe('削除', () => {
    it('削除ボタンをクリックするとAPIが呼ばれる');
    it('削除成功後に一覧から消える');
  });
});

describe('リマインダー通知', () => {
  it('Socket.IOのnotificationイベント受信でSnackbarが表示される');
});
